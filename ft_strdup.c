#include "libft.h"

char	*ft_strdup(const char *s1)
{
	int		slen;
	char	*str;
	int		i;

	slen = ft_strlen(s1);
	str = (char *)malloc(sizeof(*str) * (slen + 1));
	if (str == NULL)
		return (NULL);
	i = 0;
	while (s1[i])
	{
		str[i] = s1[i];
		i++;
	}
	str[i] = '\0';
	return (str);
}
