#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	char	*fin;

	fin = NULL;
	while (1)
	{
		if (*s == (char)c)
			fin = (char *)s;
		if (*s == '\0')
			return (char *)fin;
		s++;
	}
}
